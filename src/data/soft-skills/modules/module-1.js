function getTopPriorities() {
  return Object.entries(selfAssessment)
    .sort((a, b) => a[1] - b[1])
    .slice(0, 3)
    .map(([skill, score]) => ({ skill, score, name: skillNames[skill] }));
}

function generatePDP() {
  const priorities = getTopPriorities();
  return priorities.map((p, i) => ({
    priority: i + 1,
    skill: p.name,
    score: p.score,
    action: pdpActions[p.skill]
  }));
}

