function findDependencies() {
  const deps = [];
  allStandups.forEach(s => {
    const allTexts = [...s.yesterday, ...s.today, ...s.blockers];
    allTexts.forEach(text => {
      const mentions = text.match(/@\\w+/g);
      if (mentions) {
        deps.push({ from: s.name, text: text, mentions: mentions });
      }
    });
  });
  return deps;
}

