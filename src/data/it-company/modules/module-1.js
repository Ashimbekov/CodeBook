function findResponsible(task) {
  const taskLower = task.toLowerCase();
  for (const member of team) {
    for (const resp of member.responsibilities) {
      if (resp.toLowerCase().includes(taskLower) || taskLower.includes(resp.toLowerCase())) {
        return member.role;
      }
    }
  }
  const keywords = {
    'API': 'Backend Developer',
    'база данных': 'Backend Developer',
    'интерфейс': 'UX/UI Designer',
    'дизайн': 'UX/UI Designer',
    'тест': 'QA Engineer',
    'деплой': 'DevOps Engineer',
    'мониторинг': 'DevOps Engineer',
    'сервер': 'DevOps Engineer',
    'приоритет': 'Product Owner',
    'архитектур': 'Tech Lead',
    'мобильн': 'Frontend Developer'
  };
  for (const [keyword, role] of Object.entries(keywords)) {
    if (taskLower.includes(keyword.toLowerCase())) {
      return role;
    }
  }
  return 'Tech Lead';
}

