function validatePlan() {
  const errors = [];
  const checks = [];

  if (sprintPlan.sprintGoal && sprintPlan.sprintGoal.length > 10) {
    checks.push('✅ Sprint Goal определён');
  } else {
    errors.push('❌ Sprint Goal не определён');
  }

  const acCount = sprintPlan.userStory.acceptanceCriteria.length;
  if (acCount >= 3) {
    checks.push('✅ AC: ' + acCount + ' критерия');
  } else {
    errors.push('❌ AC: менее 3 критериев');
  }

  const taskCount = sprintPlan.tasks.length;
  if (taskCount >= 5 && taskCount <= 7) {
    checks.push('✅ Задач: ' + taskCount + ' (от 5 до 7)');
  } else {
    errors.push('❌ Задач: ' + taskCount + ' (должно быть 5-7)');
  }

  const totalSP = sprintPlan.tasks.reduce((sum, t) => sum + t.estimate, 0);
  if (totalSP <= 21) {
    checks.push('✅ Общая оценка: ' + totalSP + ' SP (≤ 21)');
  } else {
    errors.push('❌ Общая оценка: ' + totalSP + ' SP (превышает 21)');
  }

  const allValid = sprintPlan.tasks.every(t => t.estimate >= 1 && t.estimate <= 5);
  if (allValid) {
    checks.push('✅ Все задачи имеют оценку от 1 до 5 SP');
  } else {
    errors.push('❌ Некоторые задачи имеют некорректную оценку');
  }

  const isValid = errors.length === 0;
  checks.push(isValid ? '✅ План валиден' : '❌ План невалиден');

  return { checks: [...checks, ...errors], isValid };
}

