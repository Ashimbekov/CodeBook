const severityWeight = { high: 3, medium: 2, low: 1 };
const effortWeight = { high: 3, medium: 2, low: 1 };

function prioritizeSmells(smells) {
  return [...smells].sort((a, b) => {
    const priorityA = severityWeight[a.severity] * 10 - effortWeight[a.effort];
    const priorityB = severityWeight[b.severity] * 10 - effortWeight[b.effort];
    return priorityB - priorityA;
  });
}

