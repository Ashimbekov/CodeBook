function findBestStory(questionIndex) {
  const q = questions[questionIndex];
  let bestStory = null;
  let bestScore = 0;
  for (const story of stories) {
    let score = 0;
    for (const tag of q.matchingTags) {
      if (story.tags.includes(tag)) score++;
    }
    if (score > bestScore) {
      bestScore = score;
      bestStory = story;
    }
  }
  return bestStory;
}

function formatSTAR(storyId) {
  const story = stories.find(s => s.id === storyId);
  if (!story) {
    console.log('История не найдена');
    return;
  }
  console.log('\\n--- STAR: ' + story.title + ' ---');
  console.log('Теги: [' + story.tags.join(', ') + ']');
  console.log('\\nSituation: ' + story.situation);
  console.log('\\nTask: ' + story.task);
  console.log('\\nAction: ' + story.action);
  console.log('\\nResult: ' + story.result);
}

