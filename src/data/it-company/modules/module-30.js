function getUTCHours(member) {
  const start = (member.workStart - member.utcOffset + 24) % 24;
  const end = (member.workEnd - member.utcOffset + 24) % 24;
  return { start, end };
}

function findOverlapHours(members) {
  const zones = {};
  members.forEach(m => {
    if (!zones[m.city]) {
      const utc = getUTCHours(m);
      zones[m.city] = { city: m.city, timezone: m.timezone, utcStart: utc.start, utcEnd: utc.end };
    }
  });
  const cities = Object.values(zones);
  const overlapStart = Math.max(...cities.map(c => c.utcStart));
  const overlapEnd = Math.min(...cities.map(c => c.utcEnd));
  return {
    zones: cities,
    hasFullOverlap: overlapStart < overlapEnd,
    overlapStart,
    overlapEnd
  };
}

function convertToLocal(utcHour, utcOffset) {
  return ((utcHour + utcOffset) % 24 + 24) % 24;
}

function findPairOverlap(city1, city2, members) {
  const m1 = members.find(m => m.city === city1);
  const m2 = members.find(m => m.city === city2);
  const utc1 = getUTCHours(m1);
  const utc2 = getUTCHours(m2);
  const start = Math.max(utc1.start, utc2.start);
  const end = Math.min(utc1.end, utc2.end);
  return { start, end, hours: end > start ? end - start : 0 };
}

