function formatSelfReview() {
  console.log('=== SELF-REVIEW: ' + selfReview.period + ' ===\\n');
  console.log('Достижения:');
  selfReview.achievements.forEach((a, i) => {
    console.log((i + 1) + '. [Проект: ' + a.project + '] ' + a.description);
    console.log('   Impact: ' + a.impact);
    console.log('   Метрики: ' + a.metrics);
  });
  console.log('\\nЗоны роста:');
  selfReview.areasOfGrowth.forEach(area => {
    console.log('- ' + area);
  });
  console.log('\\nЦели на следующий период:');
  selfReview.goalsForNextPeriod.forEach(goal => {
    console.log('Objective: ' + goal.objective);
    goal.keyResults.forEach((kr, i) => {
      console.log('  KR' + (i + 1) + ': ' + kr);
    });
  });
}

function formatAgenda() {
  console.log('\\n=== AGENDA: 1-on-1 — ' + agenda.date + ' ===\\n');
  console.log('Темы для обсуждения:');
  agenda.topics.forEach((t, i) => {
    console.log((i + 1) + '. [' + t.type.toUpperCase() + '] ' + t.title);
    console.log('   ' + t.details);
  });
  console.log('\\nAction items с прошлого 1-on-1:');
  agenda.actionItemsFromLast.forEach(ai => {
    const icon = ai.status === 'done' ? '[x]' : ai.status === 'in-progress' ? '[~]' : '[ ]';
    console.log(icon + ' ' + ai.item);
  });
}

